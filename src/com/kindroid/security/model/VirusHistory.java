package com.kindroid.security.model;

public class VirusHistory {
	private long time;
	private int virus_num;
	private int care_num;
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public int getVirus_num() {
		return virus_num;
	}
	public void setVirus_num(int virus_num) {
		this.virus_num = virus_num;
	}
	public int getCare_num() {
		return care_num;
	}
	public void setCare_num(int care_num) {
		this.care_num = care_num;
	}
	

}
